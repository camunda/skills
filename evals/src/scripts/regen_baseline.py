"""Regenerate a scenario's ``baseline.json`` from its most recent run.

Reads the latest ``.eval`` log for ``--scenario <id>`` from
``evals/logs/`` and rewrites ``evals/scenarios/<id>/baseline.json``
with the run's model, observed pass rate, token band, and duration
band. Baselines are model-specific — the file records which model
produced it.

Filters by arm (default ``with_skill``) because both arms write to
the same log directory — picking simply the most-recent log without
an arm filter would conflate them.

Engineer reviews the diff before committing — never blanket-regen
across multiple scenarios in one go.

This is the v1 stub. Follow-up PRs will tighten the diff output and add
``--all`` rejection (force per-scenario invocation).
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

try:
    from inspect_ai.log import list_eval_logs, read_eval_log
except ImportError:  # pragma: no cover
    list_eval_logs = None
    read_eval_log = None

from core.paths import EVALS_ROOT, SCENARIOS_DIR

LOGS_DIR = EVALS_ROOT / "logs"


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--scenario", required=True)
    parser.add_argument("--arm", default="with_skill")
    parser.add_argument("--log-dir", default=LOGS_DIR, type=Path)
    args = parser.parse_args()

    scenario_dir = SCENARIOS_DIR / args.scenario
    if not scenario_dir.exists():
        print(f"scenario not found: {args.scenario}", file=sys.stderr)
        return 2

    if list_eval_logs is None:
        print("inspect-ai not installed; cannot read logs", file=sys.stderr)
        return 2

    infos = [
        info for info in list_eval_logs(str(args.log_dir))
        if args.scenario in info.name
    ]
    if not infos:
        print(f"no logs found under {args.log_dir} for scenario {args.scenario}", file=sys.stderr)
        return 2

    # Sort newest-first; pick the first one matching the target arm so
    # that re-running the script after a multi-arm sweep still lands on
    # the right run.
    infos.sort(key=lambda i: i.mtime, reverse=True)
    chosen = None
    for info in infos:
        log = read_eval_log(info.name, header_only=True)
        arm = (log.eval.task_args or {}).get("arm")
        if arm == args.arm:
            chosen = info
            break
    if chosen is None:
        print(
            f"no {args.scenario} logs found for arm={args.arm} under {args.log_dir}",
            file=sys.stderr,
        )
        return 2

    log = read_eval_log(chosen.name)
    # Capture the model the run used. Baselines are model-specific —
    # re-running the same scenario on a different model is a different
    # baseline (rewrite, not blend). A future enhancement can store
    # one file per (scenario, model); v1 stores the latest model and
    # leaves the comparison loop to read this field.
    model = getattr(log.eval, "model", None) or "unknown"
    target = scenario_dir / "baseline.json"

    # Merge with existing baseline.json so that calling this for one arm
    # doesn't clobber the other arm's block. Both arms typically share
    # the same model (it's a property of the sweep, not the arm) — we
    # write the latest run's model field unconditionally.
    existing = {}
    if target.exists():
        try:
            existing = json.loads(target.read_text())
        except json.JSONDecodeError:
            existing = {}

    # Drop legacy cost_band_usd if present — Inspect doesn't expose cost
    # on the log object, so the field was always [0, 0]; computing it
    # honestly would need a per-model price table that lives elsewhere.
    existing.pop("cost_band_usd", None)

    baseline = {
        **existing,
        "model": model,
        args.arm: {
            "pass_rate": _pass_rate(log),
            "tokens": _band(_total_tokens(log), 0.85, 1.15),
            "duration_s": _band(_duration_s(log), 0.7, 1.3),
        },
        "_note": "Regenerated by `uv run evals-regen-baseline`. Review the diff before committing.",
    }
    # Recompute the top-level pass_rate as the mean of the per-arm
    # pass_rates so it reflects whichever arms are present in the file.
    arm_pass_rates = [
        v["pass_rate"]
        for v in baseline.values()
        if isinstance(v, dict) and "pass_rate" in v
    ]
    baseline["pass_rate"] = (
        sum(arm_pass_rates) / len(arm_pass_rates) if arm_pass_rates else 0.0
    )

    target.write_text(json.dumps(baseline, indent=2) + "\n")
    print(f"wrote {target} (from {Path(chosen.name).name}, arm={args.arm}, model={model})")
    return 0


def _band(value: float, lo_mult: float, hi_mult: float) -> list[float]:
    return [round(value * lo_mult, 2), round(value * hi_mult, 2)]


# Inspect's letter-grade scorers (model_graded_qa, model_graded_fact)
# emit "C" / "P" / "I" as the raw per-sample score; the accuracy()
# metric converts to 1.0 / 0.5 / 0.0. Mirror that here so the
# arm-level pass_rate properly blends numeric scorers (mean) with
# letter scorers (accuracy). NOTE: Inspect already stores the
# computed metric values at log.results.scores[*].metrics[*].value
# — reading those directly is even cleaner than re-aggregating from
# raw sample values, but we keep the raw-sample path for now so a
# single function handles all scorer shapes.
_LETTER_TO_FLOAT = {"C": 1.0, "P": 0.5, "I": 0.0}


def _pass_rate(log) -> float:
    """Mean of all per-scorer values across all samples.

    For a multi-scorer task (e.g. rocket-launch with cluster + lint +
    CPT, or dev-routing with skill-load + model_graded_qa), this
    averages every scorer's value across every sample — matching
    what the Inspect dashboard reports per scorer column, then
    collapsed to one arm-level number. With max-samples=N and M
    scorers, it's (sum of N*M values) / (N*M).
    """
    samples = getattr(log, "samples", None) or []
    values = []
    for sample in samples:
        scores = getattr(sample, "scores", None) or {}
        for score in scores.values():
            v = getattr(score, "value", None)
            if isinstance(v, (int, float)):
                values.append(float(v))
            elif isinstance(v, str) and v.upper() in _LETTER_TO_FLOAT:
                values.append(_LETTER_TO_FLOAT[v.upper()])
    return sum(values) / len(values) if values else 0.0


def _total_tokens(log) -> float:
    stats = getattr(log, "stats", None)
    if stats is None:
        return 0.0
    usage = getattr(stats, "model_usage", None) or {}
    return float(sum(getattr(e, "total_tokens", 0) or 0 for e in usage.values()))


def _duration_s(log) -> float:
    """Average per-sample wall time (sample.total_time, in seconds).

    Inspect tracks duration per sample, not on the log-level stats
    object — `stats.duration` doesn't exist. With max-samples=1 (our
    default) this is just the run's wall time.
    """
    samples = getattr(log, "samples", None) or []
    if not samples:
        return 0.0
    times = [float(getattr(s, "total_time", 0) or 0) for s in samples]
    return sum(times) / len(times)


if __name__ == "__main__":
    sys.exit(main())
