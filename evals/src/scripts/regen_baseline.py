"""Regenerate a scenario's ``baseline.json`` from its most recent run.

Reads the latest ``.eval`` log for ``--scenario <id>`` from
``evals/logs/`` and rewrites ``evals/scenarios/<id>/baseline.json``
with the run's model, observed pass rate, token band, and duration
band. Baselines are model-specific — the file records which model
produced it.

Filters by arm (default ``with_skill``) because both arms write to
the same log directory — picking simply the most-recent log without
an arm filter would conflate them.

Engineer reviews the diff before committing — never blanket-regen
across multiple scenarios in one go.

This is the v1 stub. Follow-up PRs will tighten the diff output and add
``--all`` rejection (force per-scenario invocation).
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

try:
    from inspect_ai.log import list_eval_logs, read_eval_log
except ImportError:  # pragma: no cover
    list_eval_logs = None
    read_eval_log = None

from core.metrics import (
    pass_rate,
    sample_duration_s,
    sample_tokens,
)
from core.paths import EVALS_ROOT, SCENARIOS_DIR

LOGS_DIR = EVALS_ROOT / "logs"


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--scenario", required=True)
    parser.add_argument("--arm", default="with_skill")
    parser.add_argument("--log-dir", default=LOGS_DIR, type=Path)
    args = parser.parse_args()

    scenario_dir = SCENARIOS_DIR / args.scenario
    if not scenario_dir.exists():
        print(f"scenario not found: {args.scenario}", file=sys.stderr)
        return 2

    if list_eval_logs is None:
        print("inspect-ai not installed; cannot read logs", file=sys.stderr)
        return 2

    infos = [
        info for info in list_eval_logs(str(args.log_dir))
        if args.scenario in info.name
    ]
    if not infos:
        print(f"no logs found under {args.log_dir} for scenario {args.scenario}", file=sys.stderr)
        return 2

    # Sort newest-first; pick the first one matching the target arm so
    # that re-running the script after a multi-arm sweep still lands on
    # the right run.
    infos.sort(key=lambda i: i.mtime, reverse=True)
    chosen = None
    for info in infos:
        log = read_eval_log(info.name, header_only=True)
        arm = (log.eval.task_args or {}).get("arm")
        if arm == args.arm:
            chosen = info
            break
    if chosen is None:
        print(
            f"no {args.scenario} logs found for arm={args.arm} under {args.log_dir}",
            file=sys.stderr,
        )
        return 2

    log = read_eval_log(chosen.name)
    # Capture the model the run used. Baselines are model-specific —
    # re-running the same scenario on a different model is a different
    # baseline (rewrite, not blend). A future enhancement can store
    # one file per (scenario, model); v1 stores the latest model and
    # leaves the comparison loop to read this field.
    model = getattr(log.eval, "model", None) or "unknown"
    target = scenario_dir / "baseline.json"

    # Merge with existing baseline.json so that calling this for one arm
    # doesn't clobber the other arm's block. Both arms typically share
    # the same model (it's a property of the sweep, not the arm) — we
    # write the latest run's model field unconditionally.
    existing = {}
    if target.exists():
        try:
            existing = json.loads(target.read_text())
        except json.JSONDecodeError:
            existing = {}

    # Per-sample baselines: each sample carries its own tokens and
    # duration bands. Adding a sample to the dataset doesn't break
    # existing samples' gates; the new one simply has no baseline
    # entry yet, which pass_fail surfaces as a warning (not a fail).
    sample_blocks: dict[str, dict] = {}
    for sample in getattr(log, "samples", None) or []:
        tokens = sample_tokens(sample)
        duration = sample_duration_s(sample)
        sample_blocks[str(sample.id)] = {
            "tokens": _band(tokens, 0.85, 1.15),
            "duration_s": _band(duration, 0.7, 1.3),
        }

    arm_block: dict = {
        "pass_rate": pass_rate(log),
        "samples": sample_blocks,
    }

    baseline = {
        **existing,
        "model": model,
        args.arm: arm_block,
        "_note": "Regenerated by `uv run evals-regen-baseline`. Review the diff before committing.",
    }
    # Recompute the top-level pass_rate as the mean of the per-arm
    # pass_rates so it reflects whichever arms are present in the file.
    arm_pass_rates = [
        v["pass_rate"]
        for v in baseline.values()
        if isinstance(v, dict) and "pass_rate" in v
    ]
    baseline["pass_rate"] = (
        sum(arm_pass_rates) / len(arm_pass_rates) if arm_pass_rates else 0.0
    )

    target.write_text(json.dumps(baseline, indent=2) + "\n")
    print(f"wrote {target} (from {Path(chosen.name).name}, arm={args.arm}, model={model})")
    return 0


def _band(value: float, lo_mult: float, hi_mult: float) -> dict[str, float]:
    """Self-documenting {low, high} band — `[lo, hi]` arrays read as lists."""
    return {
        "low": round(value * lo_mult, 2),
        "high": round(value * hi_mult, 2),
    }


if __name__ == "__main__":
    sys.exit(main())
